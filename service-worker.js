const CACHE = "step-tracker-v1";
const ASSETS = [
  "/",
  "/index.html",
  "/tracker.html",
  "/history.html",
  "/settings.html",
  "/css/style.css",
  "/js/main.js",
  "/js/tracker.js",
  "/js/ui-charts.js",
  "/manifest.json",
];

self.addEventListener("install", (e) => {
  e.waitUntil(caches.open(CACHE).then((c) => c.addAll(ASSETS)));
});
self.addEventListener("fetch", (e) => {
  e.respondWith(
    caches
      .match(e.request)
      .then(
        (r) => r || fetch(e.request).catch(() => caches.match("/index.html"))
      )
  );
});
